package com.example.house_renting_md6.model;

public class Time {
    private String year;
    private String month;

    public Time() {
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }
}

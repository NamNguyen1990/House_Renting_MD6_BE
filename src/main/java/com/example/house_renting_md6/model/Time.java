package com.example.house_renting_md6.model;

public class Time {
    private int year;
    private int month;

    public Time() {
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }
}
